function buildAllMonthlyTemplates(): Record<FortuneMonth, Record<FortuneNumber, FortuneMonthlyTemplate>> {
  const result = {} as Record<FortuneMonth, Record<FortuneNumber, FortuneMonthlyTemplate>>;

  for (let month = 1; month <= 12; month += 1) {
    const monthKey = month as FortuneMonth;
    const monthly = {} as Record<FortuneNumber, FortuneMonthlyTemplate>;
    const monthOverride = monthKey >= 2 ? monthlyOverrides[monthKey] : undefined;

    for (let number = 1; number <= 9; number += 1) {
      const numberKey = number as FortuneNumber;
      monthly[numberKey] =
        monthKey === 1
          ? januaryTemplates[numberKey]
          : monthOverride?.[numberKey] ?? generateMonthlyTemplate(monthKey, numberKey);
    }

    result[monthKey] = monthly;
  }

  return result;
}

export const fortuneMonthlyTemplates = buildAllMonthlyTemplates();

export function getFortuneMonthlyTemplate(month: number, number: number): FortuneMonthlyTemplate | null {
  if (!isFortuneMonth(month) || !isFortuneNumber(number)) {
    return null;
  }

  if (month === 1) {
    return januaryTemplates[number];
  }

  const override = month >= 2 ? monthlyOverrides[month]?.[number] : undefined;
  if (override != null) {
    return override;
  }

  return generateMonthlyTemplate(month, number);
}

