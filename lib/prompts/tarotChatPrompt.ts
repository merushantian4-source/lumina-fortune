type TarotPromptTheme = "love" | "marriage" | "work" | "money" | "relationship" | "future" | "health" | null;

function formatSpreadCards(cards: DrawnTarotCard[]): string {
  return cards
    .map((draw, index) => {
      const orientation = draw.reversed ? "逆位置" : "正位置";
      const meaning = draw.reversed ? draw.card.reversedMeaning : draw.card.uprightMeaning;
      return [
        `${index + 1}. ${draw.position}: ${draw.card.nameJa}（${orientation}）`,
        `- 象徴キーワード: ${draw.card.symbolKeywords.join(" / ")}`,
        `- 絵の描写: ${draw.card.imageDescription}`,
        `- 今回の向きの読みの核: ${meaning}`,
      ].join("\n");
    })
    .join("\n");
}

function buildThemeLens(theme: TarotPromptTheme): string {
  if (theme === "love") {
    return "テーマは恋愛です。感情の揺れ、距離感、言葉の選び方、関係の流れを中心に読み解いてください。";
  }
  if (theme === "marriage") {
    return "テーマは結婚です。価値観のすり合わせ、現実的な準備、関係の成熟度、今後の歩調を中心に読み解いてください。";
  }
  if (theme === "work") {
    return "テーマは仕事です。役割、判断、職場の流れ、成果の出し方、進め方のコツを中心に読み解いてください。";
  }
  if (theme === "money") {
    return "テーマはお金です。使い方・守り方・整え方、焦りや不安との向き合い方、流れの見立てを中心に読み解いてください。";
  }
  if (theme === "relationship") {
    return "テーマは人間関係です。距離感、境界線、伝え方、関係の流れと整え方を中心に読み解いてください。";
  }
  if (theme === "future") {
    return "テーマは未来です。近い未来の流れ、選択肢ごとの空気感、今の行動がどうつながるかを中心に読み解いてください。";
  }
  if (theme === "health") {
    return `テーマは健康運です。これは健康運（流れ）を見るものであり、診断ではないことを前提にしてください。
- これは健康運（流れ）を見るものであり、診断ではない
- 体のエネルギーの流れとして読む
- 心身バランスを中心に解釈する
- 医療判断や病名は出さない
- 医療診断をしない
- 臓器や病気の断定をしない
- 「体の流れ」「エネルギー状態」「休息」「整え方」を中心に読む
- 恋愛やLINEなどの文言は絶対に出さない`;
  }
  return "テーマに合わせて、相談内容の文脈に沿って読み解いてください。";
}

