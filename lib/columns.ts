function getResolvedArticles(): ColumnArticle[] {
  return COLUMN_ARTICLES.map((article) => COLUMN_ARTICLE_OVERRIDES[article.slug] ?? article);
}

export function listColumnArticles(category: ColumnCategory | "すべて" = "すべて"): ColumnArticle[] {
  const articles = getResolvedArticles();
  if (category === "すべて") return articles;
  return articles.filter((article) => article.category === category);
}

export function getColumnArticle(slug: string): ColumnArticle | null {
  return getResolvedArticles().find((article) => article.slug === slug) ?? null;
}

export function listColumnCategories(): ColumnCategory[] {
  return Array.from(new Set(getResolvedArticles().map((article) => article.category)));
}

export function getColumnDisplayContent(
  article: ColumnArticle,
  isMember: boolean
): ColumnDisplayContent {
  const shouldGate = ENABLE_MEMBERSHIP_GATING && article.requiresMembership && !isMember;
  return {
    preview: article.preview,
    full: shouldGate ? [] : article.full,
    showFull: !shouldGate,
  };
}
