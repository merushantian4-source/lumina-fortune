function hasDate(dates: readonly string[], date: string): boolean {
  return dates.includes(date);
}

function getHolidayEntry(date: string) {
  return lightCalendar2026.holidays.find((holiday) => holiday.date === date) ?? null;
}

function buildBaseBadges(date: string): LightDayBadge[] {
  const badges: LightDayBadge[] = [];
  if (hasDate(lightCalendar2026.luckyDays.tenshaNichi, date)) badges.push(BADGE_MASTER.tensha);
  if (hasDate(lightCalendar2026.luckyDays.ichiryumanbai, date)) badges.push(BADGE_MASTER.ichiryumanbai);
  if (hasDate(lightCalendar2026.luckyDays.taian, date)) badges.push(BADGE_MASTER.taian);
  if (hasDate(lightCalendar2026.luckyDays.tsuchinotomiNoHi, date)) badges.push(BADGE_MASTER.tsuchinotomi);
  if (hasDate(lightCalendar2026.luckyDays.toraNoHi, date)) badges.push(BADGE_MASTER.tora);
  if (hasDate(lightCalendar2026.luckyDays.miNoHi, date)) badges.push(BADGE_MASTER.mi);
  if (getHolidayEntry(date)) badges.push(BADGE_MASTER.holiday);
  if (hasDate(lightCalendar2026.luckyDays.fujoujuNichi, date)) badges.push(BADGE_MASTER.fujouju);
  return sortBadges(badges);
}

function buildHeadline(primaryBadge: LightDayBadge | null, badges: LightDayBadge[]): string {
  const hasCaution = badges.some((badge) => badge.type === "fujouju");
  const strongCount = badges.filter((badge) => badge.tone === "best" || badge.tone === "good").length;
  if (!primaryBadge) return "流れを整えて過ごしたい日";
  if (primaryBadge.type === "tensha" && strongCount >= 3) return "特別感の強い追い風を感じやすい日";
  if (hasCaution && primaryBadge.type !== "fujouju") return "吉意はあるけれど慎重さも忘れたくない日";
  if (primaryBadge.type === "tensha") return "新しい願いに光を乗せやすい特別日";
  if (primaryBadge.type === "ichiryumanbai") return "小さな一歩が未来の実りにつながりやすい日";
  if (primaryBadge.type === "taian") return "安心感のある穏やかな吉日";
  if (primaryBadge.type === "tsuchinotomi") return "金運と祈りの流れが濃く宿りやすい日";
  if (primaryBadge.type === "tora") return "軽やかに動くほど追い風を感じやすい日";
  if (primaryBadge.type === "mi") return "金運や美意識を整えるのに向く日";
  if (primaryBadge.type === "holiday") return "暮らしの呼吸を整えやすい祝日";
  return "無理に進めず整えることを優先したい日";
}

