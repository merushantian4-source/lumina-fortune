function buildActionTip(primaryBadge: LightDayBadge | null, badges: LightDayBadge[]): string | undefined {
  const has = (type: LightDayBadgeType) => badges.some((badge) => badge.type === type);
  if (!primaryBadge) return undefined;
  if (primaryBadge.type === "tensha") {
    return "新しい挑戦、使い始め、申込み、告知、買い物のスタートにおすすめ。";
  }
  if (primaryBadge.type === "ichiryumanbai") {
    return has("tora")
      ? "発信開始、学びのスタート、購入、仕事の着手に。"
      : "申込み、口座づくり、勉強開始、発信の初投稿に向く日。";
  }
  if (primaryBadge.type === "taian") return "予約、相談、顔合わせ、提出ごとなどを穏やかに進めたい日に。";
  if (primaryBadge.type === "tsuchinotomi" || primaryBadge.type === "mi") {
    return "財布の整理、出費の見直し、参拝、持ち物の手入れに。";
  }
  if (primaryBadge.type === "tora") return "使い始め、旅の計画、前向きな買い物、発信スタートに。";
  return undefined;
}

function buildCaution(primaryBadge: LightDayBadge | null, badges: LightDayBadge[]): string | undefined {
  const hasCaution = badges.some((badge) => badge.type === "fujouju");
  if (!hasCaution) return undefined;
  if (primaryBadge?.type === "fujouju") {
    return "契約や勝負ごとは慎重に。見直しや保留の判断も吉です。";
  }
  return "勢いだけで決めず、条件や段取りをあと一度だけ確認すると安心です。";
}

export function getLightCalendarUi(date: string): LightDayUi {
  const badges = buildBaseBadges(date);
  const primaryBadge = pickPrimaryBadge(badges);
  return {
    date,
    badges,
    primaryBadge,
    headline: buildHeadline(primaryBadge, badges),
    softMessage: buildSoftMessage(date, primaryBadge, badges),
    actionTip: buildActionTip(primaryBadge, badges),
    caution: buildCaution(primaryBadge, badges),
  };
}

export function getCellAccent(date: string) {
  const primary = getLightCalendarUi(date).primaryBadge;
  if (!primary) return "normal" as const;
  if (primary.tone === "best") return "gold" as const;
  if (primary.tone === "good") return "warm" as const;
  if (primary.tone === "caution") return "muted" as const;
  return "normal" as const;
}

function buildLuckyDaysData(): Record<string, LuckyDayRecord> {
  const data: Record<string, LuckyDayRecord> = {};

  const ensure = (date: string): LuckyDayRecord => {
    const existing = data[date];
    if (existing) return existing;
    const next: LuckyDayRecord = { lucky: [] };
    data[date] = next;
    return next;
  };

  const pushLucky = (dates: readonly string[], kind: LuckyDayKind) => {
    for (const date of dates) {
      const entry = ensure(date);
      if (!entry.lucky.includes(kind)) entry.lucky.push(kind);
    }
  };

  const pushBad = (dates: readonly string[], kind: UnluckyDayKind) => {
    for (const date of dates) {
      const entry = ensure(date);
      entry.bad ??= [];
      if (!entry.bad.includes(kind)) entry.bad.push(kind);
    }
  };

  pushLucky(lightCalendar2026.luckyDays.ichiryumanbai, "ichiryumanbaibi");
  pushLucky(lightCalendar2026.luckyDays.tenshaNichi, "tenshabi");
  pushLucky(lightCalendar2026.luckyDays.toraNoHi, "tora-no-hi");
  pushLucky(lightCalendar2026.luckyDays.miNoHi, "mi-no-hi");
  pushLucky(lightCalendar2026.luckyDays.tsuchinotomiNoHi, "tsuchinotomi-no-hi");
  pushBad(lightCalendar2026.luckyDays.fujoujuNichi, "fujouju-nichi");

  for (const date of lightCalendar2026.luckyDays.taian) {
    ensure(date).rokuyo = "大安";
  }
  for (const date of Array.from(
    new Set([...Object.keys(data), ...lightCalendar2026.holidays.map((holiday) => holiday.date)])
  )) {
    ensure(date).ui = getLightCalendarUi(date);
  }

  return data;
}

export const LUCKY_DAYS_DATA: Record<string, LuckyDayRecord> = buildLuckyDaysData();

export function getLightCalendarLabels(date: string): LightCalendarDateLabel[] {
  return getLightCalendarUi(date)
    .badges
    .filter((badge) => badge.type !== "holiday")
    .map((badge) => badge.label as LightCalendarDateLabel);
}

export function getHolidayLabel(date: string): string | null {
  return getHolidayEntry(date)?.label ?? null;
}

export function getDayMeta(date: string) {
  return {
    labels: getLightCalendarLabels(date),
    holiday: getHolidayLabel(date),
    ui: getLightCalendarUi(date),
  };
}

export function getLuckyDaysForMonth(month: string): Record<string, LuckyDayRecord> {
  const result: Record<string, LuckyDayRecord> = {};
  for (const [dateKey, value] of Object.entries(LUCKY_DAYS_DATA)) {
    if (dateKey.startsWith(`${month}-`)) {
      result[dateKey] = value;
    }
  }
  return result;
}
