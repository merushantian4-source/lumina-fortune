function normalizeCardName(name: string): string {
  return name.replace(/のカード/g, "").trim();
}

function resolveCardVoiceKey(cardName: string, reversed?: boolean): string | null {
  const card = findTarotCardByJaName(normalizeCardName(cardName));
  if (!card) return null;

  const majorMatch = card.code.match(/^major-\d+-([a-z-]+)$/);
  if (majorMatch) {
    return `major_${majorMatch[1]}${reversed ? "_reversed" : ""}`;
  }

  const minorMatch = card.code.match(/^minor-(cups|swords|wands|pentacles)-(ace|two|three|four|five|six|seven|eight|nine|ten|page|knight|queen|king)$/);
  if (!minorMatch) return null;

  const suitMap: Record<string, string> = {
    cups: "cup",
    swords: "sword",
    wands: "wand",
    pentacles: "pentacle",
  };
  const rankMap: Record<string, string> = {
    ace: "ace",
    two: "2",
    three: "3",
    four: "4",
    five: "5",
    six: "6",
    seven: "7",
    eight: "8",
    nine: "9",
    ten: "10",
    page: "page",
    knight: "knight",
    queen: "queen",
    king: "king",
  };

  return `${suitMap[minorMatch[1]]}_${rankMap[minorMatch[2]]}${reversed ? "_reversed" : ""}`;
}

export function resolveCardVoice(card?: { name?: string | null; reversed?: boolean }): string {
  const name = card?.name?.trim();
  if (!name) return "今日は、心の声に近いところから一日を整えてみてください。";
  const key = resolveCardVoiceKey(name, card?.reversed);
  return (key ? cardVoices[key as keyof typeof cardVoices] : null) ?? "今日は、足元の感覚をたしかめながら静かに進んでみてください。";
}

export function resolveCardModes(card?: { name?: string | null; reversed?: boolean }): string[] {
  const name = card?.name?.trim();
  if (!name) return ["流れの整え", "気持ちの確認", "足元の見直し"];
  const key = resolveCardVoiceKey(name, card?.reversed);
  const modes = key ? cardModes[key as keyof typeof cardModes] : null;
  return modes && modes.length > 0 ? [...modes] : ["流れの整え", "気持ちの確認", "足元の見直し"];
}

export function resolveCategoryReading(
  card: { name?: string | null; reversed?: boolean } | undefined,
  category: TarotReadingCategory
): string {
  const name = card?.name?.trim();
  if (!name) return "今日の流れに合うところから、無理なく整えていくことが助けになります。";
  const key = resolveCardVoiceKey(name, card?.reversed);
  const readings = key ? cardCategoryReadings[key as keyof typeof cardCategoryReadings] : null;
  return readings?.[category] ?? "今日の流れに合うところから、無理なく整えていくことが助けになります。";
}

function hashModeSeed(seed: string): number {
  let hash = 0;
  for (let index = 0; index < seed.length; index += 1) {
    hash = (hash * 31 + seed.charCodeAt(index)) >>> 0;
  }
  return hash;
}

export function selectCardMode(
  card?: { name?: string | null; reversed?: boolean },
  seed = ""
): string {
  const modes = resolveCardModes(card);
  if (modes.length === 0) return "流れの整え";
  if (!seed) return modes[0];
  return modes[hashModeSeed(seed) % modes.length] ?? modes[0];
}

export function resolveCardVoiceProfile(
  card?: { name?: string | null; reversed?: boolean },
  seed = ""
): { cardVoice: string; cardModes: string[]; selectedMode: string } {
  const cardVoice = resolveCardVoice(card);
  const cardModes = resolveCardModes(card);
  const selectedMode = selectCardMode(card, seed);
  return { cardVoice, cardModes, selectedMode };
}
