function buildMinorSuitCategoryReadings(
  prefix: "wand" | "cup" | "sword" | "pentacle",
  suitThemes: {
    general: string;
    love: string;
    work: string;
    money: string;
    health: string;
  },
  rankFocus: Record<string, string>
): Record<string, CategoryReadingMap> {
  const result: Record<string, CategoryReadingMap> = {};
  for (const [rank, focus] of Object.entries(rankFocus)) {
    result[`${prefix}_${rank}`] = {
      general: `${focus}が静かに表へ出やすい日です。${suitThemes.general}`,
      love: `恋愛では、${focus}が鍵になりやすいでしょう。${suitThemes.love}`,
      work: `仕事や役割では、${focus}として流れが見えやすい日です。${suitThemes.work}`,
      money: `お金や現実面では、${focus}が表に出やすくなります。${suitThemes.money}`,
      health: `心身の面では、${focus}として現れやすい日です。${suitThemes.health}`,
    };
    result[`${prefix}_${rank}_reversed`] = {
      general: `${focus}を整え直す流れが出やすい日です。${suitThemes.general}`,
      love: `恋愛では、${focus}を見直すことがテーマになりやすいでしょう。${suitThemes.love}`,
      work: `仕事や役割では、${focus}の整え直しとして流れが見えやすい日です。${suitThemes.work}`,
      money: `お金や現実面では、${focus}を立て直す流れが表に出やすくなります。${suitThemes.money}`,
      health: `心身の面では、${focus}をゆるやかに見直す時間になりやすいです。${suitThemes.health}`,
    };
  }
  return result;
}

