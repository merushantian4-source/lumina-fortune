function buildMinorSuitVoices(
  prefix: "wand" | "cup" | "sword" | "pentacle",
  entries: Record<string, [string, string]>
): Record<string, string> {
  const voices: Record<string, string> = {};
  for (const [rank, [upright, reversed]] of Object.entries(entries)) {
    voices[`${prefix}_${rank}`] = upright;
    voices[`${prefix}_${rank}_reversed`] = reversed;
  }
  return voices;
}

function buildMinorSuitModes(
  prefix: "wand" | "cup" | "sword" | "pentacle",
  entries: Record<string, [string[], string[]]>
): Record<string, string[]> {
  const modes: Record<string, string[]> = {};
  for (const [rank, [upright, reversed]] of Object.entries(entries)) {
    modes[`${prefix}_${rank}`] = upright;
    modes[`${prefix}_${rank}_reversed`] = reversed;
  }
  return modes;
}

