import { replyStyle } from "./reply-style";

type CardLite = {
  name: string;
  reversed?: boolean;
};

function formatCardList(cards: CardLite[]): string {
  return cards
    .map((c, i) => `${i + 1}枚目: ${c.name}${c.reversed ? "(逆位置)" : ""}`)
    .join("\n");
}

